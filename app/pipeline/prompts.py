EXTRACT_INSTRUCTION = (
    "Extract EVERY clinical entity that was MENTIONED in the transcript into the schema. "
    "Be exhaustive: decision-critical history and results are easy to miss — losing them is "
    "the most harmful error. Attach provenance span ids to every item; if you cannot cite a "
    "span, do not output it.\n"
    "Set `referenced_patient` to WHO the complaints are about (the person discussed, not "
    "necessarily the speaker; 'patient' if the speaker describes their own symptoms).\n"
    "CAPTURE THESE SECTIONS WHENEVER THEY ARE DISCUSSED (do not leave them empty if the "
    "transcript mentions them):\n"
    "- `past_medical_history`: every pre-existing / chronic condition and prior procedure the "
    "patient already has (e.g. diabetes, hypertension, prior surgery) WITH its stated duration "
    "or onset (e.g. 'diabetes for 8 years'). Smoking, alcohol and other substance/social "
    "history also belong here when stated (e.g. 'smoked 1 pack/day, reduced last year'). These "
    "are the risk factors a clinician relies on — never drop them.\n"
    "- `family_history`: conditions or procedures in relatives (e.g. 'father had bypass surgery "
    "at 52'). Record the relative and the stated age/detail.\n"
    "- `investigations`: every test/result discussed — ECG, stress test/TMT, echo, labs — and "
    "CRUCIALLY their findings and VALUES, copied verbatim (e.g. 'ECG: mild ST-segment "
    "depression', 'TMT positive', 'ejection fraction 48%', 'LDL 168 mg/dL', 'HDL low', "
    "'triglycerides high', 'LVH on echo'). Each numeric value is high-value — do not summarize "
    "it away or omit the number.\n"
    "CERTAINTY — keep suspected and confirmed separate (do NOT upgrade a possibility to a "
    "diagnosis):\n"
    "- Put tentative / suspected / 'possible' / 'suggests' / 'rule-out' / differential findings "
    "in `assessment` (e.g. 'positive stress test suggests possible myocardial ischemia; "
    "concerning for coronary artery disease'). Include differentials that were named (e.g. "
    "'palpitations — query AF / SVT / PVC').\n"
    "- Put only CONFIRMED / established conditions in `diagnosis` (e.g. an echo-confirmed LVH, a "
    "stated dyslipidemia). If the doctor only raised a possibility, it is an assessment, not a "
    "diagnosis.\n"
    "NUMBERS (safety-critical): preserve every number, age, duration, dose, measurement, "
    "percentage, lab value, distance and date EXACTLY as transcribed — copy the numeric token, "
    "never round, normalize, infer, or drop it.\n"
    "MEDICATIONS (safety-critical): capture each under `medications_discussed` exactly as "
    "spoken. Copy `dose`, `route`, `frequency`, and `duration` VERBATIM from the transcript — "
    "NEVER normalize, round, or substitute a 'typical' or 'standard' value, and NEVER rename "
    "the drug to a similar real medication. If a spoken dose looks unusual (e.g. 'Pantoprazole "
    "1 mg'), still record it verbatim. Put the exact source phrase in `verbatim_text`. If a "
    "field was not spoken, leave it null — never fill it from medical knowledge.\n"
    "For each `examination` finding, set `region` to a canonical lowercase value: one of "
    "throat, nose, ear, oral_cavity, neck, chest, abdomen, cardiovascular, respiratory, "
    "neuro, skin, general. Map pharynx / tonsils / oropharynx / posterior pharyngeal wall "
    "→ throat."
)

RISK_INSTRUCTION = (
    "Identify risk indications ALREADY PRESENT in the conversation (red-flag symptoms, "
    "allergies / adverse drug reactions, stated abnormal vitals). For each marker, cite the "
    "transcript span ids in `evidence_span_ids` AND quote the exact triggering words in "
    "`evidence_text` (e.g. the patient's own sentence).\n"
    "- Respect NEGATION: a patient denying a symptom or allergy ('no allergies', 'no chest "
    "pain') is NOT a risk — either omit it or emit it at severity 'info' with a clearly "
    "'denied / absent' message.\n"
    "- Do NOT flag the doctor's screening QUESTION as a finding; flag only what was actually "
    "reported.\n"
    "- Do NOT emit medication_mentioned or dosage_mentioned markers — those are produced "
    "deterministically from the grounded extraction.\n"
    "Do NOT diagnose or recommend. This is an attention aid for the reviewing clinician only."
)
